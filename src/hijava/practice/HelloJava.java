package hijava.practice;

public class HelloJava {

	// 주석예시입니다
	
	public static void main(String[] args) {
		HelloJava.say("Hi!!");
		
		System.out.println("Hello Java!!");
	}
	/*
	 * 주석을 이어서 
	 * 작성하는 경우 예시입니다
	 */

	
	public static void say(String msg) {
		System.out.println(msg);
	}
}
